var contractAddress = "0x9256E378452D2f63446918FfA3E686E74c70A9f6";

( async() => {
    if(window.ethereum){
        await window.ethereum.send("eth_requestAccounts");
        window.web3 = new Web3(window.ethereum);
    }
    var accounts = await web3.eth.getAccounts();
    account = accounts[0]

    contract = await new web3.eth.Contract(ABI, contractAddress);
    if(contract != null)
        document.getElementById("maxSupply").textContent = await contract.methods.maxSupply().call();
        mintPrice = await contract.methods.mintPrice().call();
        maxQuantity = await contract.methods.maxPerWallet().call();
        minQuantity = 1;


    updateTotalSupply();
})();


const updateTotalSupply = async() =>{
    document.getElementById("totalSupply").textContent = await contract.methods.totalSupply().call();
}

document.getElementById("mint").onclick = () =>{
    quantity = document.getElementById("quantity").value;

    var passe = true;

    if(quantity < minQuantity || quantity > maxQuantity){
        passe = false;
    }

    if(passe){
        quantity = quantity * 1 //converting quantity from string to number
        if(mintPrice != null && maxQuantity != null){
            mint();
        }
        
    }else{
        alert("please enter a number between 1 and 3");
    }
    
}

const mint = async() =>{
	await contract.methods.mintNFT(quantity).send({from : account, value : (mintPrice * quantity).toString()})
    updateTotalSupply();
    //alert(typeof( (mintPrice * quantity).toString() ));
}